/**
 * op_add - function to sum 2 values
 * @a: first int value
 * @b: second int value
 *
 * Return: int/result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract from 2 values
 * @a: first int value
 * @b: second int value
 *
 * Return: int/result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply 2 values
 * @a: first int value
 * @b: second int value
 *
 * Return: int/result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide 2 integers
 * @a: first int value
 * @b: second int value
 *
 * Return: int/result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return the remainder of the division of 2 values
 * @a: first int value
 * @b: second int value
 *
 * Return: int/result
 */
int op_mod(int a, int b)
{
	return (a % b);
}

